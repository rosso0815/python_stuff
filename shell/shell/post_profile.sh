#-----------------------------------------------------------------------------
# shortcut to mvim
#-----------------------------------------------------------------------------

2g () {

	echo "start vim ${@}"
	MVIM="mvim"
	MVIM_SL=$( "$MVIM" --serverlist )

	if [[ -n $MVIM_SL ]] ; then
		"$MVIM" --servername "${MVIM_SL}" --remote-tab "${@}"
	else
		"$MVIM" -g "${@}"
	fi

}

